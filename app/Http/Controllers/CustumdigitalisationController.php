     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'request' => 'required|string|max:255',
        ]);

        $companyRequest = new CustomDigitalisation();
        $companyRequest->name = $request->input('name');
        $companyRequest->typerequest = $request->input('typerequest');
        $companyRequest->request = $request->input('request');
        $companyRequest->user_id = auth()->user()->id; 
        $companyRequest->save();


        return view('customdigitalisation.requestsuccessful');
        
    }

